package cheer;

public class Cheer {

	private int cheerID;
	private String userID;
	private String cheerContent;
	private int cheerAvailable;
	
	
	public int getCheerID() {
		return cheerID;
	}
	public void setCheerID(int cheerID) {
		this.cheerID = cheerID;
	}
	public String getUserID() {
		return userID;
	}
	public void setUserID(String userID) {
		this.userID = userID;
	}
	public String getCheerContent() {
		return cheerContent;
	}
	public void setCheerContent(String cheerContent) {
		this.cheerContent = cheerContent;
	}
	public int getCheerAvailable() {
		return cheerAvailable;
	}
	public void setCheerAvailable(int cheerAvailable) {
		this.cheerAvailable = cheerAvailable;
	}
	
	
}
